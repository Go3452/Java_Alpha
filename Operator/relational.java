/*
 * Relational operator - [ == , != , > , < , >= , <= ]
 */
package Operator;

public class relational {

	public static void main(String[] args) {
	 int a=23;
	 int b=75;
	 System.out.println("a == b : " + (a==b));
	 System.out.println("a != b : " + (a!=b));
	 System.out.println("a > b : " + (a>b));
	 System.out.println("a < b : " + (a<b));
	 System.out.println("a >= b : " + (a>=b));
	 System.out.println("a <= b : " + (a<=b));
	}

}

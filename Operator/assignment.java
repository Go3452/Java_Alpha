/*
 * Assignment Operator - [ = , += , -= , *= , /= ]
 */
package Operator;

public class assignment {

	public static void main(String[] args) {
		int a=10;
		System.out.println(a);
		a+=10;
		System.out.println(a);
		a-=10;
		System.out.println(a);
		a*=10;
		System.out.println(a);
		a/=10;
		System.out.println(a);
	}

}

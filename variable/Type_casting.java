/*
 * Type casting is the forceful conversion of variable.
 *  ( Explicit conversion )
 */
package variable;

public class Type_casting {

	public static void main(String[] args) {
		float a = 34.566f;
		int b = (int)a;
		System.out.println(a);
		System.out.println(b);
		char ch='a';
		char ch1='b';
		int num1=ch;
		int num2=ch1;
		System.out.println(num1);
		System.out.println(num2);
	}

}

/* 
 * Byte = 1 byte
 * short = 2 byte
 * char = 2 byte
 * boolean = 1 bit
 * int = 4 byte
 * long = 8 bytes
 * float = 4 bytes (decimel)
 * double = 8 bytes (decimel)
 */
package variable;

public class datatype {

	public static void main(String[] args) {
		short s = 6345;
		char ch = 'a';
		byte b = -128;
		boolean t = true;
		int n = 1234556432;
		long ans = 123456432;
		float f = 3.54f;
		double d = 34.56433;
		System.out.println(s);
		System.out.println(ch);
		System.out.println(b);
		System.out.println(t);
		System.out.println(n);
		System.out.println(ans);
		System.out.println(f);
		System.out.println(d);

	}

}
